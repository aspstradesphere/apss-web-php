                                        $docs_found = true;

                                        // Clean up filename for display
                                        $name = pathinfo($file, PATHINFO_FILENAME);
                                        $name = str_replace(['_', '-'], ' ', $name);
                                        $name = ucwords($name);

                                        // URL encode the filename to handle spaces and special chars
