                                        echo '    <div class="document-meta">' . strtoupper($ext) . ' File</div>';
                                        echo '    <a href="' . $file_url . '" target="_blank" class="document-btn">View Document</a>';
                                        echo '  </div>';
                                        echo '</div>';
                                    }
                                }
                            }

                            if (!$docs_found) {
