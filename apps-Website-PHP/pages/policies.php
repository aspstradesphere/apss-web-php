                            $files = scandir($doc_dir);
                            $docs_found = false;

                            foreach ($files as $file) {
                                if ($file !== '.' && $file !== '..') {
                                    $ext = pathinfo($file, PATHINFO_EXTENSION);
                                    // Only show typical document types
